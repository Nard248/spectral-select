def evaluate_results(results: List[Dict[str, Any]]) -> None:
    """Evaluate and compare results from all configurations."""
    print("\n" + "=" * 80)
    print("PIPELINE INTEGRATION TEST RESULTS")
    print("=" * 80)

    # Summary table
    print(f"\n{'Config Name':<30} {'Status':<10} {'Bands':<8} {'Time (s)':<10} {'Top Influence':<15}")
    print("-" * 80)

    successful = 0
    for r in results:
        status = "✅ OK" if r["success"] else "❌ FAIL"
        n_bands = r.get("n_bands", "-")
        duration = f"{r.get('duration', 0):.1f}"
        top_inf = f"{r.get('top_influence', 0):.6f}" if r.get("top_influence") else "-"

        print(f"{r['config_name']:<30} {status:<10} {n_bands:<8} {duration:<10} {top_inf:<15}")

        if r["success"]:
            successful += 1

    print("-" * 80)
    print(f"Total: {successful}/{len(results)} configurations succeeded")

    # Detailed analysis of successful runs
    successful_results = [r for r in results if r["success"]]
    if successful_results:
        print("\n" + "=" * 80)
        print("DETAILED ANALYSIS")
        print("=" * 80)

        # Compare excitation distribution
        print("\nExcitation wavelength distribution (bands per excitation):")
        print(f"{'Config':<25}", end="")

        # Get all excitations
        all_excitations = set()
        for r in successful_results:
            if r.get("excitation_distribution"):
                all_excitations.update(r["excitation_distribution"].keys())
        all_excitations = sorted(all_excitations)

        for ex in all_excitations:
            print(f"{ex:>8}nm", end="")
        print()
        print("-" * (25 + len(all_excitations) * 10))

        for r in successful_results:
            print(f"{r['config_name']:<25}", end="")
            dist = r.get("excitation_distribution", {})
            for ex in all_excitations:
                count = dist.get(ex, 0)
                print(f"{count:>10}", end="")
            print()

        # Compare influence score statistics
        print("\nInfluence score statistics:")
        print(f"{'Config':<25} {'Min':<12} {'Max':<12} {'Mean':<12} {'Std':<12}")
        print("-" * 70)

        def fmt_stat(val: float) -> str:
            """Format stat value adaptively."""
            if abs(val) < 0.001 and val != 0:
                return f"{val:<12.2e}"
            return f"{val:<12.6f}"

        for r in successful_results:
            if r.get("influence_stats"):
                stats = r["influence_stats"]
                print(f"{r['config_name']:<25} "
                      f"{fmt_stat(stats['min'])} "
                      f"{fmt_stat(stats['max'])} "
                      f"{fmt_stat(stats['mean'])} "
                      f"{fmt_stat(stats['std'])}")

        # Top wavelengths comparison
        print("\nTop 5 wavelengths for each config:")
        print("-" * 80)

        for r in successful_results:
            print(f"\n{r['config_name']}:")
            if r.get("top_wavelengths"):
                for i, wl in enumerate(r["top_wavelengths"][:5], 1):
                    # Use adaptive formatting for influence scores
                    inf = wl['influence']
                    inf_str = f"{inf:.2e}" if abs(inf) < 0.001 and inf != 0 else f"{inf:.6f}"
                    print(f"  {i}. Ex={wl['excitation']}nm, Em={wl['emission']}nm, "
                          f"Influence={inf_str}")

    # Check result consistency
    print("\n" + "=" * 80)
    print("RESULT CONSISTENCY CHECK")
    print("=" * 80)

