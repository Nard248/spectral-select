def run_single_config(
    config_params: Dict[str, Any],
    data: SpectraData,
    output_base: Path,
    model_path: Path,
) -> Dict[str, Any]:
    """Run analysis with a single configuration using a pre-trained model."""
    config_name = config_params.pop("name")
    logger.info(f"\n{'=' * 60}")
    logger.info(f"Running config: {config_name}")
    logger.info(f"{'=' * 60}")

    result = {
        "config_name": config_name,
        "success": False,
        "error": None,
    }

    start_time = time.time()

    try:
        # Create output directory for this config
        output_dir = output_base / config_name
        output_dir.mkdir(parents=True, exist_ok=True)

        # Create config with shared model path
        config = Config(
            sample_name=f"Lichens_2_{config_name}",
            output_dir=output_dir,
            model_path=model_path,  # Use the pre-trained shared model
            device="cpu",  # Use CPU for consistency
            n_baseline_patches=20,  # Reduced for speed
            patch_size=32,
            save_tiff_layers=False,  # Skip for speed
            save_visualizations=False,  # Skip for speed
            **config_params,
        )

        # Create analyzer - it will load the pre-trained model
        analyzer = Analyzer(config)

        # Run fit (model will be loaded, not trained)
        analyzer.fit(data)

        # Get results
        wavelengths = analyzer.get_wavelengths()

        # Extract statistics
        result["success"] = True
        result["n_bands"] = len(wavelengths)
        result["duration"] = time.time() - start_time

        # Influence stats
        influences = [w.influence_score for w in wavelengths]
        result["influence_stats"] = {
            "min": float(np.min(influences)),
            "max": float(np.max(influences)),
            "mean": float(np.mean(influences)),
            "std": float(np.std(influences)),
        }
        result["top_influence"] = influences[0] if influences else 0

        # Excitation distribution
        ex_dist: Dict[float, int] = {}
        for w in wavelengths:
            ex = w.excitation_nm
            ex_dist[ex] = ex_dist.get(ex, 0) + 1
        result["excitation_distribution"] = ex_dist

        # Top wavelengths
        result["top_wavelengths"] = [
            {
                "excitation": w.excitation_nm,
                "emission": w.emission_nm,
                "influence": w.influence_score,
                "rank": w.rank,
            }
            for w in wavelengths[:10]
        ]

        # Save results
        analyzer.save_results(output_dir)

        logger.info(f"✅ Config {config_name} completed: {len(wavelengths)} bands selected")

    except Exception as e:
        result["error"] = str(e)
        result["duration"] = time.time() - start_time
        logger.error(f"❌ Config {config_name} failed: {e}")
        import traceback
        traceback.print_exc()

    return result


def train_shared_model(data: SpectraData, output_base: Path, num_epochs: int = 20) -> Path:
    """Train a single model to be shared across all configurations.

    Returns the path to the saved model weights.
    """
    print("\n" + "=" * 80)
    print("STEP 1: Training shared autoencoder model")
    print("=" * 80)

    model_path = output_base / "shared_model.pth"

    # Check if model already exists
    if model_path.exists():
        print(f"✅ Found existing model at {model_path}")
        return model_path

    print(f"Training with {num_epochs} epochs...")
    start_time = time.time()

    # Create a config just for training
    train_config = Config(
        sample_name="Lichens_2_model_training",
        output_dir=output_base / "model_training",
        model_path=model_path,
        device="cpu",
        n_baseline_patches=20,
        patch_size=32,
        n_bands_to_select=20,
        save_tiff_layers=False,
        save_visualizations=False,
    )

    # Create analyzer and train
    analyzer = Analyzer(train_config)

    # Patch training to use fewer epochs
    def fast_train():
        patched_train_new_model(analyzer, num_epochs=num_epochs)
    analyzer._train_new_model = fast_train

    # Run fit to train the model
    analyzer.fit(data)

    duration = time.time() - start_time
    print(f"✅ Model trained and saved to {model_path} ({duration:.1f}s)")

    return model_path


def main():
    """Run the full pipeline integration test."""
    print("=" * 80)
    print("SPECTRAL_SELECT FULL PIPELINE INTEGRATION TEST")
    print("=" * 80)
    print(f"Testing with 10 configurations, 20 epochs for model training")
    print()

    # Load data (using trusted pickle file from preprocessing)
    data_path = PROJECT_ROOT / "Data/processed/Lichens_2/temp_cropped/lichens_data_cropped.pkl"

    if not data_path.exists():
        print(f"❌ Data file not found: {data_path}")
        print("Please ensure the Lichens_2 dataset is preprocessed.")
        sys.exit(1)

    print(f"Loading data from: {data_path}")
    data = SpectraData.from_pickle(data_path)
    print(f"Data loaded: {data.n_excitations} excitations, spatial shape {data.spatial_shape}")
    print()

    # Setup output directory
    output_base = PROJECT_ROOT / "results/integration_test_results"
    output_base.mkdir(parents=True, exist_ok=True)
    print(f"Output directory: {output_base}")

    # STEP 1: Train model ONCE
    model_path = train_shared_model(data, output_base, num_epochs=20)

    # STEP 2: Run all configs using the shared model
    print("\n" + "=" * 80)
    print("STEP 2: Running 10 configurations with shared model")
    print("=" * 80)

    configs = get_test_configurations()
    print(f"Running {len(configs)} configurations...")

    # Run all configs
    results = []
    total_start = time.time()

    for i, config_params in enumerate(configs, 1):
        print(f"\n[{i}/{len(configs)}] ", end="")
        result = run_single_config(
            config_params.copy(),  # Copy to avoid modifying original
            data,
            output_base,
            model_path=model_path,  # Pass the shared model
        )
        results.append(result)

    total_duration = time.time() - total_start
    print(f"\n\nTotal execution time: {total_duration:.1f} seconds")

    # Evaluate results
    evaluate_results(results)

    # Final verdict
    successful = sum(1 for r in results if r["success"])
    print(f"\n{'=' * 80}")
    if successful == len(results):
        print("🎉 ALL CONFIGURATIONS PASSED - Pipeline integration verified!")
    elif successful > 0:
        print(f"⚠️  PARTIAL SUCCESS - {successful}/{len(results)} configurations worked")
    else:
        print("❌ ALL CONFIGURATIONS FAILED - Pipeline has issues!")
    print("=" * 80)

    return 0 if successful > 0 else 1


if __name__ == "__main__":
    sys.exit(main())
